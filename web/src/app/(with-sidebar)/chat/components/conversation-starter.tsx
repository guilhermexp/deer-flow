import { motion } from "framer-motion";

import { cn } from "~/lib/utils";

import { Welcome } from "./welcome";

const questions = [
  "Quantas vezes a Torre Eiffel é mais alta que o edifício mais alto do mundo?",
  "Quantos anos dura uma bateria Tesla média comparada a um motor a gasolina?",
  "Quantos litros de água são necessários para produzir 1 kg de carne bovina?",
  "Quantas vezes a velocidade da luz é mais rápida que a velocidade do som?",
];
export function ConversationStarter({
  className,
  onSend,
}: {
  className?: string;
  onSend?: (message: string) => void;
}) {
  return (
    <div className={cn("flex flex-col items-center", className)}>
      <div className="pointer-events-none fixed inset-0 flex items-center justify-center">
        <Welcome className="pointer-events-auto mb-15 w-[75%] -translate-y-24" />
      </div>
      <ul className="flex flex-wrap">
        {questions.map((question, index) => (
          <motion.li
            key={`${index}-${question}`}
            className="flex w-1/2 shrink-0 p-2 active:scale-105"
            style={{ transition: "all 0.2s ease-out" }}
            initial={{ opacity: 0, y: 24 }}
            animate={{ opacity: 1, y: 0 }}
            exit={{ opacity: 0, y: -20 }}
            transition={{
              duration: 0.2,
              delay: index * 0.1 + 0.5,
              ease: "easeOut",
            }}
          >
            <div
              className="cursor-pointer rounded-xl border border-white/10 bg-white/[0.05] px-4 py-4 text-gray-400 backdrop-blur-sm transition-all duration-300 hover:bg-white/[0.08] hover:text-gray-300 hover:border-white/20"
              onClick={() => {
                onSend?.(question);
              }}
            >
              {question}
            </div>
          </motion.li>
        ))}
      </ul>
    </div>
  );
}
